$(document).ready(function(){
      $(".jplayer_1").jPlayer({
        ready: function () {
          $(this).jPlayer("setMedia", {
            title: "Big Buck Bunny",
            m4a: "music/Делай что Ты хочешь во мне.mp3",
            
            volume: 100,
          });
        },
        swfPath: "/js",
        supplied: "m4a, ogv, mp3",
        cssSelectorAncestor: ".player_container_1",
        cssSelector: {
          title: "#title",
          play: ".fa-play",
          pause: ".fa-pause",
          stop: ".fa-stop",
          mute: "#mute",
          unmute: "#unmute",
          currentTime: "#currentTime",
         duration: "#duration"
        },
        size: {
          width: "0px",
          height: "0px"
        }
      });
    });

